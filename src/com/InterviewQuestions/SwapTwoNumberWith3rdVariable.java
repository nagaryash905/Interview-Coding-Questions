package com.InterviewQuestions;

public class SwapTwoNumberWith3rdVariable {

	public static void main(String[] args) {

		int a=10,b=20;
		
		System.out.println("Before Swapping: "+a+" and "+b);
		
		int temp=a;
		a=b;
		b=temp;
		
		System.out.println("After Swapping: "+a+" and "+b);
		
		
		
		
	}

}
